export function getTechniqueById(id: string): Technique | undefined {
  return techniques.find((t) => t.id === id);
}

export function getTechniquesByCategory(category: Category): Technique[] {
  return techniques.filter((t) => t.category === category);
}

export function getRelatedTechniques(ids: string[]): Technique[] {
  return ids.map((id) => getTechniqueById(id)).filter(Boolean) as Technique[];
}
